				<h1>Pétition pour une révision constitutionnelle en faveur des haïtiens vivant à l’étranger.</h1>
				<p class="lead">
					En signant cette pétition, ajoutez votre voix à celles de l’Honorable Jerry Tardieu et aux millions de compatriotes vivant à l’étranger qui exigent que la constitution haïtienne prévoie des sièges au parlement haïtien pour des représentants de la diaspora !
				</p>
				<h3>Message du député Jerry Tardieu</h3>
				<p>
					En 2019, lors de la dernière session législative de la cinquantième législature, le parlement haïtien aura à disposer d’une éventuelle déclaration d’amendement. En tant que parlementaire, je compte supporter fermement cet amendement notamment afin d’obtenir, entre autres, que les Haïtiens de la diaspora bénéficient d’une représentation officielle au Parlement haïtien. Notons qu’un nombre important de pays ont décidé d’accorder une place de choix à leur diaspora notamment en permettant que celle-ci jouisse d’une représentation parlementaire.
				</p>
				<p>
					En France par exemple, la révision constitutionnelle du 23 juillet 2008 a introduit la représentation à l’Assemblée Nationale des Français de l’étranger. Cette innovation institutionnelle est en cours dans de nombreux pays de la région. A coté des onze Députés représentant les Français de l’étranger siègent également douze Sénateurs représentant les Français établis hors de France.</p>
				<p>
					L’Algérie également aménage la représentation de ses ressortissants de l’étranger dans les assemblées législatives. D’autres pays du Nord et du Sud qui acceptent désormais le cumul de la nationalité adaptent leur législation en conséquence. On peut citer le Canada, la France, l’Italie, le Maroc, l’Algérie, les Philippines, la République Dominicaine.
				</p>
				<p>
					Sans une réforme constitutionnelle, on ne parviendra jamais à rattacher notre diaspora à l’alma mater. Elle s’impose pour définitivement intégrer la diaspora qui s’et révélée une composante majeure et indéniable de l’histoire haïtienne contemporaine. Ce ne sera que justice de par son importance (plus de 2 millions de personnes), de par les ressources qu’elle dégage, de par ses potentialités productives et créatrices, de par son apport à la vie culturelle et économique, de par sa contribution aux luttes pour la démocratie.</p>
				<p>
					J’invite mes compatriotes vivant a l’étranger a signer cette pétition pour m’appuyer dans cet effort. Avec ses propres représentants au parlement, notre diaspora devient une force incontournable pour le relèvement national.
				</p>
