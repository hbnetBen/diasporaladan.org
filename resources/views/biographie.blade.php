				<p>
					JerryTardieu est un entrepreneur haïtien profondément engagé dansla vie économique

mais aussi sociale, académique, associative et politique de son pays. En 25 ans de

carrière, il a été Président Directeur-Général de nombreuses entreprises Haïtiennes dans différents secteurs d'activités allant de l'industrie du cuir à l'immobilier en passant

par l’hôtellerie. En marge de ses activités dans le privé, Jerry Tardieu est également

impliqué dans de nombreuses activités citoyennes et communautaires. Il est fondateur

et porte-parole du « Kolektif Abitan Petyonvil » un regroupement citoyen engagé dans

la commune de Pétion-Ville.
				</p>
				<p>
					Jerry Tardieu a fait ses études primaires et secondaires à l’Institution Saint-Louis

de Gonzague. Jerry Tardieu est Président de l’Association des anciens diplômés de

l’Université de Harvard résidant en Haïti. Il a été Vice-président de la Chambre de

Commerce et d’Industrie.
				</p>
				<p>
					Ses compétences académiques tant dans le domaine

privé que public lui valent d’être régulièrement consulté dans la mise en place de

partenariats institutionnels impliquant l’État et le patronat. En tant que Président du

Comité des Relations Internationales de la Chambre de Commerce et d’Industrie d’Haïti

(CCIH), dans les années 2000, Jerry Tardieu a représenté le secteur privé haïtien dans de

nombreuses missions mixtes réunissant décideurs privés et publics.
				</p>
				<p>
					En 2009, Jerry Tardieu a été membre de la Commission Présidentielle de travail sur la

Constitution ayant pour mandat de formuler des propositions d’amendements à la loi-
mère du pays.
				</p>
				<p>
					Jerry Tardieu est l’auteur de plusieurs ouvrages traitant d’Haïti et des défis de

développement et de modernisation.

En 2006, il a publié « Haïti : l’Avenir en Face » un essai à succès. En 2014, il a publié « Investir

et s’Investir en Haïti : Un acte de foi » un livre entretien qui a également enregistré des

ventes records en Haïti et en diaspora.
				</p>
				<p>
					Jerry Tardieu intervient régulièrement comme conférencier dans des universités

haïtiennes et étrangères sur les thèmes d’intérêt divers faisant l’objet de ses publications

ou articles.
				</p>
				<p>
					Jerry Tardieu est récipiendaire de nombreux prix et médailles demérite et reconnaissance.

Très jeune, En 1993, il a été distingué du prix « Jeune entrepreneur de l’année » octroyé

par le Ministère de l’Education Nationale, de la Jeunesse, des Sports et du Service Civique.

En 2011, il a reçu conjointement avec Wycleff Jean le « Aksyon Visionary Award ». En

2014, Il a été élevé au rang de “Défenseur de l’Ordre de la Plume et du Sceau” par le

Syndicat des notaires des juridictions d’Haiti. Jerry Tardieu a reçu la médaille d’honneur

de la ville de North Miami dont le Maire en a fait un citoyen honoraire en 2014 en lui

remettant les clefs de cette ville. Jerry Tardieu est récipiendaire du << Leadership

Award for innovation, commitment and community service >> attribué par la TRIBUNE

NATIONALE DE LA JEUNESSE HAITIENNE (TNJH). Il est également récipiendaire du

HARVARD -SOGEBANK fellowship award.
