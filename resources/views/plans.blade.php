				<h1>Le plan d’actions de Jerry Tardieu pour les Haïtiens vivant à l’étranger :</h1>
				<p>
					L’Honorable Jerry Tardieu a dévoilé son plan d’action pour la communauté haïtienne expatriée dans une publication parue en Juin 2016 aux éditions Henri Deschamps.
				</p>
				<p>
					Dans cette plaquette, le parlementaire estime que le devoir patriotique de tout haïtien est de se battre pour favoriser entre les Haïtiens/Haïtiennes de l’intérieur et de l’extérieur une citoyenneté commune, un sentiment d’appartenance à un seul peuple, une seule culture, de nourrir un grand rêve de bonheur.
				</p>
				<p>
					Pour livrer ce combat, et le gagner, l’Honorable Tardieu envisage une action triple au niveau de la chambre des députés. En plus de ladémarche visant à amender la constitution dans le sens que la loi mère permette une représentation parlementaire des haïtiens de l’étranger, le député Jerry Tardieu envisage ce qui suit :
				</p>
				<p class="lead">
					1 : obtenir la participation aux scrutins présidentiels
				</p>
				<p>
					A cour terme, Tardieu compte mener un plaidoyer auprès des autorités décisionnelles pour exiger que nos compatriotes vivant à l’étranger puissent au moins voter aux élections présidentielles. Cette décision est seulement tributaire d’un ensemble de dispositions d’ordre technique et infrastructurelles. Avec la volonté politique nécessaire, elle peut se faire.
				</p>
				<p>
					Partant des dispositions légales en vigueur, les citoyens Haïtiens absents du pays devraient pouvoir jouir pleinement de leurs droits quels que soient le pays où ils vivent. Refuser aux compatriotes de la diaspora un droit fondamental consacré par la constitution de 1987 amendée est un crime de lèse-patrie.
				</p>
				<p class="lead">
					2- promouvoir une nouvelle loi sur la nationalité
				</p>
				<p>
					A moyen terme, le député Tardieu compte supporter, avec l’aide de plusieurs collègues de la Chambre, des partenaires de la société civile et de collaborateurs immédiats, une proposition de loi sur la nationalité, compte tenu du titre II de la Constitution amendée portant sur la nationalité haïtienne (articles 10 à 12). Précisément, l’article 10 dispose : «Les règles relatives à la nationalité haïtiennes sont réglées par la loi.» Les articles 12.1 à 15 sont abrogés.</p>
				<p>
					Le député Tardieu espère que cette proposition de loi soit déposée le plus rapidement que possible. L’heure est à l’urgence. En effet, la société haïtienne ne peut faire l’économie d’une réflexion sur un phénomène sociodémographique d’une ampleur sans précédent qui caractérise la vie haïtienne d’aujourd’hui. Les difficultés économiques et les troubles politiques des dernières décennies ont porté des familles haïtiennes à faire naître leurs enfants en dehors d’Haïti. Plus que jamais les classes moyennes, les élites haïtiennes et de plus en plus d’Haïtiens issus des masses défavorisées, se reproduisent à l’étranger.
				</p>
				<p>
				De nos jours, il est rare que des parents haïtiens disposant d’un visa d’entrée dans un pays du Nord ne fassent pas le choix de faire naître leurs enfants en terre étrangère. Ce faisant, ils estiment offrir à leurs progénitures de meilleures chances de vie puisque la détention d’une autre nationalité confère des acquis sociaux et autres bénéfices aux nouveaux nés.
				</p>
				<p>
					Nous avons donc un problème fondamental à résoudre. Si aujourd’hui, nous ne prenons pas les dispositions pour intégrer celles et ceux d’entre nous qui naissent à « l’étranger » le pays risque d’en payer un prix élevé sinon tout simplement de disparaître. Cette affirmation peut paraître démesurée. Mais l’est-elle vraiment ? Le député Tardieu n’hésite pas à parler de « disparition » car une nation qui voit ses fils et filles, en proportion chaque année plus grande, naître en terre étrangère est vouée au déclin et à la disparition.
				</p>
